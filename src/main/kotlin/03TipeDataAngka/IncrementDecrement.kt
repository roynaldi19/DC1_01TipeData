package `03TipeDataAngka`

fun main() {
    var score = 10
    score = score + 1
    println("Nilai dengan operator +: $score")

    var anotherScore = 10
    anotherScore++
    println("Nilai dengan operator ++: $anotherScore")

    println("\n=====================================\n")

    var score2 = 10
    score2 = score2 - 1
    println("Nilai dengan operator -: $score2")

    var anotherScore2 = 10
    anotherScore2--
    println("Nilai dengan operator --: $anotherScore2")
}