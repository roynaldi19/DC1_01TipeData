package `03TipeDataAngka`

fun main() {
    var a = 5
    var b = 10

    a++
    println("Nilai a setelah increment: $a")

    b--
    println("Nilai b setelah decrement: $b")

    var x = 5
    var y = 10

    x += 3
    println("Nilai x setelah x += 3: $x")

    y -= 2
    println("Nilai y setelah y -= 2: $y")

    x *= 2
    println("Nilai x setelah x *= 2: $x")

    y /= 4
    println("Nilai y setelah y /= 4: $y")
}